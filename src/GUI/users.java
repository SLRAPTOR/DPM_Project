        Object[] userData = userModel.getSelectedUserDatabyId(userid);

        NIC.setText((String) userData[0]);
        Name.setText((String) userData[1]);
        jComboBox1.setSelectedItem((String) userData[2]);
        Email.setText((String) userData[3]);
        ConNo.setText((String) userData[4]);

        Password.setText((String) userData[5]);

        jCheckBox1.setSelected((boolean) userData[6]);

