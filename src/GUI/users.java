    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String nic = NIC.getText();
        String name = Name.getText();
        String cat = (String) jComboBox1.getSelectedItem();
        String email = Email.getText();
        String connNo = ConNo.getText();

        char[] charpassword = Password.getPassword();
        String password = new String(charpassword, 0, charpassword.length);

        boolean isactive = jCheckBox1.isSelected();

        String result = userModel.updateuser(nic, name, cat, email, connNo, password, isactive);

        JOptionPane.showConfirmDialog(rootPane, result, "Message ", -1, 1);

        userModel.loadTable("", 0, userTableModel);

    }//GEN-LAST:event_jButton3ActionPerformed

